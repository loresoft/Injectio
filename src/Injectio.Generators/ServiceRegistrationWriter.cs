        codeBuilder
            .AppendLine()
            .DecrementIndent()
            .AppendLine(")")
            .DecrementIndent()
            .AppendLine(");")
            .AppendLine();
    }

    private static void WriteServiceGeneric(
        IndentedStringBuilder codeBuilder,
        ServiceRegistration serviceRegistration,
        string serviceMethod,
        string serviceType)
    {
        var describeMethod = GetServiceDescriptorMethod(serviceRegistration);

        var implementationType = serviceRegistration.ImplementationType.HasValue()
            ? serviceRegistration.ImplementationType
            : serviceType;

        codeBuilder
            .Append("global::Microsoft.Extensions.DependencyInjection.Extensions.ServiceCollectionDescriptorExtensions.")
            .Append(serviceMethod)
            .AppendLine("(")
            .IncrementIndent()
            .AppendLine("serviceCollection,")
            .Append("global::Microsoft.Extensions.DependencyInjection.ServiceDescriptor.")
            .Append(describeMethod)
            .Append("<")
            .AppendIf("global::", !serviceType.StartsWith("global::"))
            .Append(serviceType);

        // don't include the implementation type if there is factory
        if (serviceRegistration.Factory.IsNullOrEmpty())
        {
            codeBuilder
                .Append(", ")
                .AppendIf("global::", !implementationType.StartsWith("global::"))
                .Append(implementationType);
        }

        codeBuilder.Append(">(");

        // write service key argument
        if (serviceRegistration.ServiceKey.HasValue())
            codeBuilder.Append(serviceRegistration.ServiceKey);

        // write factory method
        if (serviceRegistration.Factory.HasValue())
        {
            bool hasNamespace = serviceRegistration.Factory?.Contains(".") == true;

            codeBuilder
                .AppendIf(", ", serviceRegistration.ServiceKey.HasValue()) // second argument if there is a service key
                .AppendIf(serviceRegistration.ImplementationType, !hasNamespace)
                .AppendIf(".", !hasNamespace)
                .Append(serviceRegistration.Factory);
        }
        else if (serviceRegistration.Registration == KnownTypes.RegistrationStrategySelfWithProxyFactoryShortName
            && serviceRegistration.ImplementationType != serviceType)
        {
            codeBuilder
                .AppendIf(", ", serviceRegistration.ServiceKey.HasValue())
                .AppendLine()
                .IncrementIndent()
                .Append("(serviceProvider")
                .AppendIf(", key", serviceRegistration.ServiceKey.HasValue())
                .Append(") => global::Microsoft.Extensions.DependencyInjection.ServiceProvider")
                .Append(serviceRegistration.ServiceKey.HasValue()
                    ? "KeyedServiceExtensions.GetRequiredKeyedService<"
                    : "ServiceExtensions.GetRequiredService<")
                .AppendIf("global::", !serviceRegistration.ImplementationType.StartsWith("global::"))
                .Append(serviceRegistration.ImplementationType)
                .Append(">(serviceProvider")
                .AppendIf(", key", serviceRegistration.ServiceKey.HasValue())
                .Append(")")
                .AppendLine()
                .DecrementIndent();
        }

        codeBuilder
            .AppendLine(")")
            .DecrementIndent()
            .AppendLine(");")
            .AppendLine();
    }

    private static void WriteDecorator(
        IndentedStringBuilder codeBuilder,
        DecoratorRegistration decorator)
    {
        if (decorator.Tags.Count > 0)
        {
            codeBuilder
                .Append("if (tagSet.Count == 0 || tagSet.Intersect(new[] { ");

            bool wroteTag = false;
            foreach (var tag in decorator.Tags)
            {
                if (wroteTag)
                    codeBuilder.Append(", ");

                codeBuilder
                    .Append("\"")
                    .Append(tag)
                    .Append("\"");

                wroteTag = true;
            }

            codeBuilder
                .AppendLine(" }).Any())")
                .AppendLine("{")
                .IncrementIndent();
        }

        var serviceType = decorator.ServiceType;
        var decoratorType = decorator.DecoratorType;
        bool hasServiceKey = decorator.ServiceKey.HasValue();
        bool isKeyed = hasServiceKey || decorator.IsAnyKey;

        // resolve the service key expression passed to the helper
        string keyExpression;
        if (decorator.IsAnyKey)
            keyExpression = "global::Microsoft.Extensions.DependencyInjection.KeyedService.AnyKey";
        else if (hasServiceKey)
            keyExpression = decorator.ServiceKey!;
        else
            keyExpression = "null";

        if (decorator.IsOpenGeneric)
        {
            codeBuilder
                .Append("global::Injectio.Internal.InjectioDecorationExtensions.DecorateOpenGeneric(")
                .AppendLine()
                .IncrementIndent()
                .AppendLine("serviceCollection,")
                .Append("typeof(")
                .AppendIf("global::", !serviceType.StartsWith("global::"))
                .Append(serviceType)
                .AppendLine("),")
                .Append("typeof(")
                .AppendIf("global::", !decoratorType.StartsWith("global::"))
                .Append(decoratorType)
                .AppendLine(")")
                .DecrementIndent()
                .AppendLine(");")
                .AppendLine();
        }
        else if (isKeyed)
        {
            codeBuilder
                .Append("global::Injectio.Internal.InjectioDecorationExtensions.DecorateKeyed<")
                .AppendIf("global::", !serviceType.StartsWith("global::"))
                .Append(serviceType)
                .AppendLine(">(")
                .IncrementIndent()
                .AppendLine("serviceCollection,")
                .Append(keyExpression)
                .AppendLine(",");

            WriteDecoratorFactory(codeBuilder, decorator, isKeyed: true);

            codeBuilder
                .AppendLine()
                .DecrementIndent()
                .AppendLine(");")
                .AppendLine();
        }
        else
        {
            codeBuilder
                .Append("global::Injectio.Internal.InjectioDecorationExtensions.Decorate<")
                .AppendIf("global::", !serviceType.StartsWith("global::"))
                .Append(serviceType)
                .AppendLine(">(")
                .IncrementIndent()
                .AppendLine("serviceCollection,");

            WriteDecoratorFactory(codeBuilder, decorator, isKeyed: false);

            codeBuilder
                .AppendLine()
                .DecrementIndent()
                .AppendLine(");")
                .AppendLine();
        }

        if (decorator.Tags.Count > 0)
        {
            codeBuilder
                .DecrementIndent()
                .AppendLine("}")
                .AppendLine();
        }
    }

    private static void WriteDecoratorFactory(
        IndentedStringBuilder codeBuilder,
        DecoratorRegistration decorator,
        bool isKeyed)
    {
        var serviceType = decorator.ServiceType;
        var decoratorType = decorator.DecoratorType;
        var qualifiedService = serviceType.StartsWith("global::") ? serviceType : "global::" + serviceType;
        var qualifiedDecorator = decoratorType.StartsWith("global::") ? decoratorType : "global::" + decoratorType;

        if (decorator.Factory.HasValue())
        {
            bool hasNamespace = decorator.Factory!.Contains(".");
            var factoryTarget = hasNamespace ? decorator.Factory! : qualifiedDecorator + "." + decorator.Factory;

            if (isKeyed)
            {
                codeBuilder
                    .Append("static (serviceProvider, serviceKey, inner) => ")
                    .Append(factoryTarget)
                    .Append("(serviceProvider, serviceKey, inner)");
            }
            else
            {
                codeBuilder
                    .Append("static (serviceProvider, inner) => ")
                    .Append(factoryTarget)
                    .Append("(serviceProvider, inner)");
            }
        }
        else
        {
            if (isKeyed)
            {
                codeBuilder
                    .Append("static (serviceProvider, serviceKey, inner) => global::Microsoft.Extensions.DependencyInjection.ActivatorUtilities.CreateInstance<")
                    .Append(qualifiedDecorator)
                    .Append(">(serviceProvider, inner)");
            }
            else
            {
                codeBuilder
                    .Append("static (serviceProvider, inner) => global::Microsoft.Extensions.DependencyInjection.ActivatorUtilities.CreateInstance<")
                    .Append(qualifiedDecorator)
                    .Append(">(serviceProvider, inner)");
            }
        }
    }

